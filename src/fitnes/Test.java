    private void jLabel56MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel56MouseClicked
        //Prikaz svih Clanova
        String hql = "from Tabela zaposleni ";
        Query query = session.createQuery(hql);

        List<Tabela> zaposleni = null;

        try {

            tx = session.beginTransaction();

            zaposleni = query.list();

            tx.commit();

        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            System.out.println(e);
        }

        StringBuilder rezultat = new StringBuilder();

        for (Tabela person1 : zaposleni) {

            rezultat.append(person1.getId());
            rezultat.append("  ");
            rezultat.append(person1.getIme());
            rezultat.append("  ");
            rezultat.append(person1.getPrezime());
            rezultat.append("  ");
            rezultat.append(person1.getAdresa());
            rezultat.append("  ");
            rezultat.append(" \n");

            svicl.setText(rezultat.toString());

        }
    }//GEN-LAST:event_jLabel56MouseClicked

